"""Unit tests for CaddyConfigManager (P-0008 Scope 1)."""
import os
from pathlib import Path
from types import SimpleNamespace
from unittest.mock import patch

import pytest

from latarnia.caddy.manager import CaddyConfigManager
from latarnia.core.config import ConfigManager


def _make_app(name, has_web_ui=True, port=8151, app_type="service"):
    """Build a minimal registry entry stand-in."""
    return SimpleNamespace(
        name=name,
        type=app_type,
        manifest=SimpleNamespace(config=SimpleNamespace(has_web_ui=has_web_ui)),
        runtime_info=SimpleNamespace(assigned_port=port),
    )


class _FakeRegistry:
    def __init__(self, apps):
        self._apps = apps

    def get_all_apps(self):
        return self._apps


def _manager(tmp_path, apps, monkeypatch, env="dev", domain="localhost"):
    cfg = ConfigManager()
    cfg.load_config()
    cfg.config.process_manager.data_dir = str(tmp_path / "data")
    app_manager = SimpleNamespace(registry=_FakeRegistry(apps))
    monkeypatch.setenv("ENV", env)
    if domain != "localhost":
        monkeypatch.setenv(f"{env.upper()}_DOMAIN", domain)
    return CaddyConfigManager(cfg, app_manager)


def test_generate_dev_localhost_site_address(tmp_path, monkeypatch):
    mgr = _manager(tmp_path, [], monkeypatch, env="dev")
    config = mgr.generate_config()
    assert config.startswith("# Generated by Latarnia (dev)")
    assert "localhost {" in config
    # Public + catch-all blocks always present
    assert "handle /auth/* {" in config
    assert "handle /docs* {" in config
    assert "handle /openapi.json {" in config
    assert "handle /* {" in config
    assert "uri /auth/verify" in config
    assert "copy_headers X-Latarnia-User X-Latarnia-App-Role X-Latarnia-Is-Super" in config


def test_generate_writes_file(tmp_path, monkeypatch):
    mgr = _manager(tmp_path, [], monkeypatch, env="dev")
    mgr.generate_config()
    expected = tmp_path / "caddy" / "latarnia.caddyfile"
    assert expected.exists()
    assert "localhost {" in expected.read_text()


