def test_deregister_regenerates_without_app(tmp_path, monkeypatch):
    apps = [_make_app("my_app", port=8151)]
    mgr = _manager(tmp_path, apps, monkeypatch, env="dev")
    with patch.object(mgr, "reload", return_value=True):
        mgr.on_app_registered("my_app-1")
        assert "/apps/my_app/" in mgr._caddyfile_path().read_text()
        # Simulate the app going away, then deregister
        apps.clear()
        mgr.on_app_deregistered("my_app-1")
        assert "/apps/my_app/" not in mgr._caddyfile_path().read_text()


def test_reload_falls_back_to_admin_api_when_no_caddy_binary(tmp_path, monkeypatch):
    mgr = _manager(tmp_path, [], monkeypatch, env="dev")
    mgr.generate_config()

    class _Resp:
        status_code = 200
        text = "ok"

    with patch("latarnia.caddy.manager.shutil.which", return_value=None), \
         patch("latarnia.caddy.manager.httpx.post", return_value=_Resp()) as post:
        assert mgr.reload() is True
        assert post.call_args.kwargs["headers"]["Content-Type"] == "text/caddyfile"


def test_hooks_are_exception_safe(tmp_path, monkeypatch):
    mgr = _manager(tmp_path, [], monkeypatch, env="dev")
    with patch.object(mgr, "generate_config", side_effect=OSError("disk full")):
        # Must not raise — request handlers depend on this.
        assert mgr.on_app_registered("x") is False
        assert mgr.on_app_deregistered("x") is False
